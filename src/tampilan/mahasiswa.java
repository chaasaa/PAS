        public void setColumnWidth(int kolom) {
            DefaultTableColumnModel dtcm = (DefaultTableColumnModel) tblmahasiswa.getColumnModel();
            tblmahasiswaColumn kolomtabel = dtcm.getColumn(kolom);
            int lebar = 0;
            int margin = 10;
            int a;
            tblmahasiswaCellRenderer renderer = kolomtabel.getHeaderRenderer();
            if (renderer == null) {
                renderer = tblmahasiswa.getTableHeader().getDefaultRenderer();
            }
            Component komponen = renderer.getTableCellRendererComponent(tblmahasiswa, tblmahasiswa.getHeaderValue(), false, false, 0, 0);
            lebar = komponen.getPreferredSize().width;
            for (a = 0; a < tblmahasiswa.getRowCount(); a++){
                renderer = tblmahasiswa.getCellRenderer(a, kolom);
                komponen = renderer.getTableCellRendererComponent(tblmahasiswa, tblmahasiswa.getValueAt(a, kolom), false, false, a, kolom);
                int lebarKolom = komponen.getPreferredSize().width;
                lebar = Math.max(lebar, lebarKolom);
            }
            lebar = lebar + margin;
            kolomtabel.setPreferredWidth(lebar);
        }
        public void setLebarKolom() {
            int a;
            for (a = 0; a < tblmahasiswa.getColumnCount(); a++) {
                setColumnWidth(a);
            } 
        }
